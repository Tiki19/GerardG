    private void btnActualizarPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarPasswordActionPerformed
        String newPassword = new String(txtPasswordNew.getPassword());
        String repeatPassword = new String(txtPasswordRepeat.getPassword());
        String codEmpleado = dtoUsuario.getCodEmpleado();
        if (!newPassword.isEmpty() && !repeatPassword.isEmpty()) {
            if (newPassword.equals(repeatPassword)) {
                String mensaje = oUsuario.actualizarPassword(newPassword, codEmpleado);
                JOptionPane.showMessageDialog(null, mensaje, "ÉXITO", JOptionPane.INFORMATION_MESSAGE);

                // Preguntar para si desea cerrar sesión para ingresar con su nueva contraseña
                String mensajeCerrarSesion = "<html>¿<b>Deseas cerrar sesión </b> para ingresar con tu nueva contraseña?</html>";
                JLabel labelMensaje = new JLabel();
                labelMensaje.setText(mensajeCerrarSesion);
                int opcionCrearUsuario = JOptionPane.showConfirmDialog(null, labelMensaje, "Confirmación", JOptionPane.YES_NO_OPTION);
                if (opcionCrearUsuario == JOptionPane.YES_OPTION) {
                    frameLogin jpLogin = new frameLogin();
                    jFrame.setVisible(false);
                    jpLogin.setVisible(true);

                } else {
                    txtPasswordNew.setText("");
                    txtPasswordRepeat.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden", "¡Error! ", JOptionPane.ERROR_MESSAGE);
                txtPasswordRepeat.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Las contraseñas no debe estar vacias", "¡Error! Datos Incompletos", JOptionPane.ERROR_MESSAGE);
            txtPasswordNew.requestFocus();
        }
    }//GEN-LAST:event_btnActualizarPasswordActionPerformed


