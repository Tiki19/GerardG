    private void btnGuardarCelularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCelularActionPerformed
        String celular = txtCelular.getText();
        if (!celular.isEmpty()) {
            if (celular.startsWith("9") && celular.length() == 9) {
                String mensaje = oEmpleado.actualizarCelular(dtoEmpleado.getCodEmpleado(), celular);
                JOptionPane.showMessageDialog(null, mensaje, "ÉXITO", JOptionPane.INFORMATION_MESSAGE);
                btnEditarCelular.setEnabled(true);
                btnGuardarCelular.setVisible(false);
                txtCelular.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(null, "Debe tener 9 dígitos y \ncomenzar con el N° 9", "¡Error! N° de Celular no válido", JOptionPane.ERROR_MESSAGE);
                txtCelular.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "El campo celular no debe estar vacío", "¡Error! Datos Incompletos", JOptionPane.ERROR_MESSAGE);
            txtCelular.requestFocus();
        }
    }//GEN-LAST:event_btnGuardarCelularActionPerformed

