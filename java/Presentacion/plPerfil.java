    public void actualizarUsername(String userName, String codEmpleado) {
        Toolkit.getDefaultToolkit().beep();
        // Actualizamos al usuario 
        String mensaje = oUsuario.actualizarUserName(userName, codEmpleado);
        JOptionPane.showMessageDialog(null, mensaje, "ÉXITO", JOptionPane.INFORMATION_MESSAGE);

        // Preguntar para si desea cerrar sesión para ingresar con su nuevo Nombre de Usuario
        String mensajeCerrarSesion = "<html>¿<b>Deseas cerrar sesión </b> para que ingreses con tu nuevo Nombre de Usuario?</html>";
        JLabel labelMensaje = new JLabel();
        labelMensaje.setText(mensajeCerrarSesion);
        int opcionCrearUsuario = JOptionPane.showConfirmDialog(null, labelMensaje, "Confirmación", JOptionPane.YES_NO_OPTION);
        if (opcionCrearUsuario == JOptionPane.YES_OPTION) {
            frameLogin jpLogin = new frameLogin();
            jFrame.setVisible(false);
            jpLogin.setVisible(true);

        } else {
            txtUserName.setText("");
        }
    }

